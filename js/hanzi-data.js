// 获取指定难度的汉字列表
export function getHanziByDifficulty(difficulty) {
    const config = HANZI_DIFFICULTY[difficulty];
    if (!config) return [];

    let result = [];
    for (const category of config.categories) {
        const items = HANZI_DATA[category] || [];
        if (config.maxItems) {
            result = result.concat(items.slice(0, config.maxItems));
        } else {
            result = result.concat(items);
        }
    }
    return result;
}

// 获取所有汉字
export function getAllHanzi() {
    let result = [];
    for (const category of Object.keys(HANZI_DATA)) {
        result = result.concat(HANZI_DATA[category]);
    }
    return result;
}
